// Derived: all scheduled changes across all products (flat list)
export function getAllScheduledChanges(products) {
  return products.flatMap(product =>
    product.scheduledPriceChanges.map(change => ({
      ...change,
      productId: product.id,
      productName: product.name,
      currentPrice: product.unitPrice,
      category: product.category,
    }))
  ).sort((a, b) => new Date(a.effectiveDate) - new Date(b.effectiveDate));
}

// Derived: all price history across all products (for Pricing log)
export function getAllPriceHistory(products) {
  return products.flatMap(product =>
    product.priceHistory.map((entry, i) => ({
      ...entry,
      productId: product.id,
      productName: product.name,
      oldPrice: i > 0 ? product.priceHistory[i - 1].price : null,
      newPrice: entry.price,
      type: 'Historical',
    }))
  ).sort((a, b) => new Date(b.date) - new Date(a.date));
}
